<h2 ime="lkiod">History</h2> <p>
The Web Hypertext Application Technology Working Group (WHATWG)
 began work on the new standard in 2004. At that time, HTML 4.01 had 
 not been updated since 2000,and the World Wide Web Consortium (W3C)
 was focusing future developments on XHTML 2.0. In 2009, the W3C allowed the
 XHTML 2.0 Working Group's charter to expire and decided not to renew it. W3C
 and WHATWG are currently working together on the development of HTML5.
The Mozilla Foundation and Opera Software presented a position paper at a World Wide
 Web Consortium (W3C) workshop in June 2004,focusing on developing technologies that
 are backward compatible with existing browsers,including an initial draft specification 
 of Web Forms 2.0. The workshop concluded with a vote—8 for, 14 against—for continuing work on HTML.
Immediately after the workshop, WHATWG was formed to start work based upon that position paper, 
and a second draft, Web Applications 1.0, was also announced.The two specifications were later merged to form HTML5.
The HTML5 specification was adopted as the starting point of the work of the new HTML working group of the W3C in 2007.
WHATWG published the First Public Working Draft of the specification on 22 January 2008.</p>
<h2 ime="ikiod">Thoughts of Flash</h2>
<p>While some features of HTML5 are often compared to Adobe Flash, the two technologies are very different.
 Both include features for playing audio and video within web pages, and for using Scalable Vector Graphics.
 However, HTML5 on its own cannot be used for animation or interactivity – it must be supplemented with CSS3 or JavaScript.
 There are many Flash capabilities that have no direct counterpart in HTML5 (see Comparison of HTML5 and Flash). HTML5's interactive
 capabilities became a topic of mainstream media around April 2010 after Apple Inc.'s then-CEO Steve Jobs issued a public
 letter titled "Thoughts on Flash" in which he concluded that "Flash is no longer necessary to watch video or consume any kind of web content"
 and that "new open standards created in the mobile era, such as HTML5, will win".This sparked a debate in web development circles 
 suggesting that, while HTML5 provides enhanced functionality, developers must consider the varying browser support of the different parts
 of the standard as well as other functionality differences between HTML5 and Flash.In early November 2011, Adobe announced that it
 would discontinue development of Flash for mobile devices and reorient its efforts in developing tools using HTML5.On July 25, 2017,
 Adobe announced that both the distribution and support of Flash will cease by the end of 2020.</p>
 
